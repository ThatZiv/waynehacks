CREATE OR REPLACE FUNCTION public.can_join_team(target_team_id bigint, user_uuid uuid)
 RETURNS boolean
 LANGUAGE sql
 STABLE
 SET search_path TO ''
AS $function$select
    (
      target_team_id is null
      and not exists (
        select 1 from public.teams
        where leader = user_uuid
          and id is distinct from target_team_id
      )
    )
    or
    (
      exists (
        select 1
        from public.teams t
        where t.id = target_team_id
          and (user_uuid = any(t.invites) or t.open_invite = true)
      )
      and (select count(*) from public.team_members where team_id = target_team_id) < 4
      and not exists (
        select 1 from public.teams
        where leader = user_uuid
          and id <> target_team_id
      )
    );$function$
;


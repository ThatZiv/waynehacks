CREATE OR REPLACE FUNCTION public.team_insert_add_leader()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO ''
AS $function$
BEGIN
  -- Only proceed if leader is provided
  IF NEW.leader IS NULL THEN
    RETURN NEW;
  END IF;

  -- If leader already has a team_members row, update it to new team_id
  IF EXISTS (SELECT 1 FROM public.team_members tm WHERE tm.id = NEW.leader) THEN
    UPDATE public.team_members
    SET team_id = NEW.id,
        created_at = now()
    WHERE id = NEW.leader;
  ELSE
    INSERT INTO public.team_members (id, team_id, created_at)
    VALUES (NEW.leader, NEW.id, now());
  END IF;

  RETURN NEW;
END;
$function$
;

create or replace view "public"."team_with_members" as  SELECT COALESCE(t.id, ('-1'::integer)::bigint) AS id,
    COALESCE(t.team_name, 'Unassigned'::text) AS team_name,
    t.leader,
    COALESCE(t.invites, '{}'::uuid[]) AS invites,
    COALESCE(t.open_invite, false) AS open_invite,
    array_agg(json_build_object('member_id', m.id, 'created_at', m.created_at, 'full_name', a.full_name, 'email', a.email, 'university', a.university) ORDER BY m.created_at) AS members,
    t.created_at
   FROM ((team_members m
     LEFT JOIN teams t ON ((t.id = m.team_id)))
     LEFT JOIN applications a ON ((a.applicant_id = m.id)))
  GROUP BY COALESCE(t.id, ('-1'::integer)::bigint), COALESCE(t.team_name, 'Unassigned'::text), t.leader, t.invites, COALESCE(t.open_invite, false), t.created_at;



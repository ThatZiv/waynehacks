CREATE OR REPLACE FUNCTION public.kick_team_member(target_team_member uuid)
 RETURNS void
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$DECLARE
  caller_user_id uuid := NULL;
  member_team_id int;
  team_leader uuid;
BEGIN
  SET search_path = pg_catalog, trusted_schema;
  -- Safely try to get auth.uid() as uuid; if invalid or missing, treat as NULL
  BEGIN
    caller_user_id := auth.uid()::uuid;
  EXCEPTION WHEN others THEN
    caller_user_id := NULL;
  END;

  IF caller_user_id IS NULL THEN
    RAISE EXCEPTION 'Unauthorized: no authenticated user';
  END IF;

  IF caller_user_id = target_team_member THEN
    RAISE EXCEPTION 'Invalid operation: You cannot kick yourself.';
  END IF;

  -- Get the target member's current team
  SELECT team_id
  INTO member_team_id
  FROM public.team_members
  WHERE id = target_team_member;

  IF member_team_id IS NULL THEN
    RAISE EXCEPTION 'Invalid operation: target member is not assigned to a team';
  END IF;

  -- Lookup the leader for that team
  SELECT leader::uuid
  INTO team_leader
  FROM public.teams
  WHERE id = member_team_id;

  IF team_leader IS NULL THEN
    -- this shoulnd't happen
    RAISE EXCEPTION 'Invalid operation: team has no leader';
  END IF;

  -- Ensure caller is the team leader
  IF caller_user_id <> team_leader THEN
    RAISE EXCEPTION 'Unauthorized: only the team leader may remove a member';
  END IF;

  -- All checks passed — perform the removal
  UPDATE public.team_members
  SET team_id = NULL
  WHERE id = target_team_member;

  RETURN;
END;$function$
;

